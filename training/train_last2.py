#!/usr/bin/env python3
"""
train_last2.py -- Two-phase YOLO26 size comparison for the Njord buoy detector.

Trains yolo26 m / n / s / l (m first: most important, then fastest-first so an
early cutoff still banks the small models). Each size is trained in TWO phases and
every model is streamed to Weights & Biases so it is retrievable later even if you
lose access to the training box.

    Phase 1 (base):     COCO-pretrained -> heavy augmentation + mosaic ON -> best.pt
    Phase 2 (finetune): load Phase-1 best.pt -> mosaic OFF (closed), low LR -> best.pt

Every problem this fixes vs train_last.py (go through them one by one):
  1. Missing size `n`               -> default sizes = m n s l (m first).
  2. Single-stage training          -> explicit two-phase base -> closed-mosaic finetune.
  3. Cloud weights never updated     -> `wandb.save(..., policy="live")` does not re-sync
     ("best.pt stuck at 17m ago"). Replaced by versioned W&B *artifacts* uploaded from
     an epoch callback + a clean final artifact per phase. Guaranteed retrievable.
  4. `scale=(0.25, 1)` tuple crash   -> Ultralytics treats `scale` as a single float
     fraction in [0,1] (cfg validation raises TypeError on a tuple). Use a float.
  5. Wrong classes (6 vs 3)          -> the labels are 3-class {green,red,cardinal} but
     data_split.yaml declares 6, so cardinal logs as "north" and e/s/w show 0 instances.
     This script generates a correct nc=3 split yaml at RUNTIME from train/val/test.txt
     (those + data_split.yaml are gitignored, so they must be fixed on the box itself).
  6. OOM / crashes                   -> multi_scale OFF by default (the 1.5x-scale OOM),
     deterministic=False (unstable 4090 kernels), phase-aware auto-resume, OOM batch
     backoff, CUDA cache cleared between runs. Safe to just re-launch after a crash.
  7. Heavy augmentation preserved    -> hsv/degrees/translate/scale/shear/perspective + mosaic.

Usage (on the GPU box, after `git pull`):
    python training/train_last2.py                 # all four sizes, full run
    python training/train_last2.py --sizes m       # just the important one
    python training/train_last2.py --batch 8       # if you still hit OOM
"""
import os
