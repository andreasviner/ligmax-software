NAMES_3 = ["green", "red", "cardinal"]


def ensure_data_yaml(script_dir):
    root = os.path.abspath(os.path.join(script_dir, "..", "navier_merged"))
    train_txt = os.path.join(root, "train.txt")
    val_txt = os.path.join(root, "val.txt")
    test_txt = os.path.join(root, "test.txt")
    out = os.path.join(root, "data_split3.yaml")

    if os.path.exists(train_txt) and os.path.exists(val_txt):
        lines = [
            "# AUTO-GENERATED by train_last2.py -- 3-class split (green/red/cardinal).",
            "# Rebuilt every run from the (gitignored) train/val/test.txt lists so it is",
            "# always correct on whatever machine trains. 'path:' omitted on purpose ->",
            "# Ultralytics resolves paths relative to THIS file's own directory.",
            "train: train.txt",
            "val: val.txt",
        ]
        if os.path.exists(test_txt):
            lines.append("test: test.txt")
        lines += ["", "nc: 3", f"names: {NAMES_3}", ""]
        with open(out, "w", encoding="utf-8") as f:
            f.write("\n".join(lines))
        print(f"[data] wrote 3-class split yaml -> {out}", flush=True)
        return out

    data_yaml = os.path.join(root, "data.yaml")
    if os.path.exists(data_yaml):
        print("[data] WARNING: train.txt/val.txt not found; falling back to data.yaml "
              "(train==val, so val metrics will be optimistic).", flush=True)
        return data_yaml
    raise SystemExit(f"[data] no dataset config found under {root}")


def sanity_check_labels(data_yaml, nc=3, sample=25):
    """Best-effort, fast: warn loudly if labels still carry class ids >= nc (i.e. the
    6-class labels were not remapped on this box -> run training/remap_6to3.py)."""
    try:
        root = os.path.dirname(os.path.abspath(data_yaml))
        train_txt = os.path.join(root, "train.txt")
        if not os.path.exists(train_txt):
            return
        with open(train_txt, encoding="utf-8") as f:
            imgs = [ln.strip() for ln in f if ln.strip()][:sample]
        worst = -1
        for img in imgs:
            lbl = os.path.join(root, img).replace("\\", "/")
            lbl = lbl.replace("/images/", "/labels/").rsplit(".", 1)[0] + ".txt"
            if not os.path.exists(lbl):
                continue
            with open(lbl, encoding="utf-8") as lf:
                for line in lf:
                    p = line.split()
                    if p:
                        worst = max(worst, int(p[0]))
        if worst >= nc:
            print(f"\n[labels] !!! WARNING: found class id {worst} but nc={nc}. Your "
                  f"labels look like the 6-class set. Run:  python training/remap_6to3.py"
                  f"  on this box, then re-launch.\n", flush=True)
        else:
            print(f"[labels] sample OK: max class id = {worst} (< nc={nc})", flush=True)
    except Exception as e:
        print(f"[labels] sanity check skipped ({type(e).__name__}: {e})", flush=True)


def get_device(arg):
    if arg:
        return arg
    return "0" if torch.cuda.is_available() else "cpu"


def empty_cuda():
    try:
        gc.collect()
        if torch.cuda.is_available():
            torch.cuda.empty_cache()
    except Exception:
        pass  # context may be corrupt after a CUDA fault; never mask the real error


# Exit code that run_loop.sh watches for to restart a FRESH process after a GPU crash.
CUDA_FAULT_EXIT = 75


def is_cuda_fault(e):
    """True for CUDA faults that CORRUPT the context -> unrecoverable in-process, the whole
    process must restart (that is what run_loop.sh does). OOM is excluded on purpose: it is
    recoverable and handled by the batch backoff."""
    if isinstance(e, torch.cuda.OutOfMemoryError):
        return False
    s = f"{type(e).__name__}: {e}".lower()
    return any(k in s for k in (
        "unspecified launch failure", "illegal memory access", "device-side assert",
        "cuda error", "acceleratorerror", "cublas", "cudnn status",
        "misaligned address", "an illegal instruction",
    ))


